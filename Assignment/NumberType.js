// This function checks if a number is positive, negative, or zero
function findnum() {
     let num = -1; // You can change this value to test different cases
     if (num > 0) {
          console.log(`${num} is an positive number`);
     }
     else if (num === 0) {
          console.log(`Its a zero : ${num}`);
     }
     else {
          console.log(`${num} is an negative number`);
     }

}
